package com.hadoit.game.engine.guardian.core.exception;

/**
 * Guardian server runtime exception
 * 
 * @author bezy
 * 
 */
public class GuardianServerException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	public GuardianServerException() {
	}

	public GuardianServerException(String message) {
		super(message);
	}

	public GuardianServerException(Throwable cause) {
		super(cause);
	}

	public GuardianServerException(String message, Throwable cause) {
		super(message, cause);
	}
}
