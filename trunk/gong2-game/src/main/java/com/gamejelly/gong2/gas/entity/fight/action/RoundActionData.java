
package com.gamejelly.gong2.gas.entity.fight.action;

import java.util.ArrayList;
import java.util.List;

public class RoundActionData {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private List<BaseAction> actions = new ArrayList<BaseAction>();

	private List<RoundBuffData> roundBuffList = new ArrayList<RoundBuffData>();

	public List<BaseAction> getActions() {
		return actions;
	}

	public void setActions(List<BaseAction> actions) {
		this.actions = actions;
	}

	public void addAction(BaseAction ba) {
		this.actions.add(ba);
	}

	public List<RoundBuffData> getRoundBuffList() {
		return roundBuffList;
	}

	public void setRoundBuffList(List<RoundBuffData> roundBuffList) {
		this.roundBuffList = roundBuffList;
	}

	public void addRoundBuffDatas(List<RoundBuffData> roundBuffList) {
		this.roundBuffList.addAll(roundBuffList);
	}
}
