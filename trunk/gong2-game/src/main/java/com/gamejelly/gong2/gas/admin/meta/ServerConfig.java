package com.gamejelly.gong2.gas.admin.meta;

import java.util.List;

import com.hadoit.game.engine.guardian.core.RemoteType;

/**
 * @author bezy 2014-2-26
 * 
 */
public class ServerConfig implements RemoteType {
	private static final long serialVersionUID = 1L;

	private boolean open;
	private int maxOnlineCount;
	private int curOnlineCount;
	private int userGroupDefault;
	private int userGroupLogin;
	private String openServerDate;
	private List<VersionData> versionDatas;

	public boolean isOpen() {
		return open;
	}

	public void setOpen(boolean open) {
		this.open = open;
	}

	public int getMaxOnlineCount() {
		return maxOnlineCount;
	}

	public void setMaxOnlineCount(int maxOnlineCount) {
		this.maxOnlineCount = maxOnlineCount;
	}

	public int getCurOnlineCount() {
		return curOnlineCount;
	}

	public void setCurOnlineCount(int curOnlineCount) {
		this.curOnlineCount = curOnlineCount;
	}

	public int getUserGroupDefault() {
		return userGroupDefault;
	}

	public void setUserGroupDefault(int userGroupDefault) {
		this.userGroupDefault = userGroupDefault;
	}

	public int getUserGroupLogin() {
		return userGroupLogin;
	}

	public void setUserGroupLogin(int userGroupLogin) {
		this.userGroupLogin = userGroupLogin;
	}

	public String getOpenServerDate() {
		return openServerDate;
	}

	public void setOpenServerDate(String openServerDate) {
		this.openServerDate = openServerDate;
	}

	public List<VersionData> getVersionDatas() {
		return versionDatas;
	}

	public void setVersionDatas(List<VersionData> versionDatas) {
		this.versionDatas = versionDatas;
	}

}
